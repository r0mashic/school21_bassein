#include <stdlib.h>
char	*ft_strdup(char *src)
{
	char	*src2;
	char	*c1;
	char	*c2;

	src2 = malloc(sizeof(src));
	c1 = src;
	c2 = src2;
	while (*c1 != '\0')
	{
		*c2 = *c1;
		c1++;
		c2++;
	}
	return (src2);
}
